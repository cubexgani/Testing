//normal console statement
console.log("Hello world");

//lambda
let hello = () => console.log("Hello world");
hello();
console.log(hello);

//function expression
let hellof = function (){
	console.log("Hello world");
}
console.log(hellof);
hellof();

//normal function
function fhello(){
	console.log("Hello world");
}
fhello();

